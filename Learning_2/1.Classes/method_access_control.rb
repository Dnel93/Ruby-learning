=begin
    Method Access Control
    * Methods are the primary interfaces to a class
    * Only expose interfaces when necessary
    * Access control restricts access to methods from outside an instance

    Method Access Control Types:
    * public: anyone can access (default)
    * protected: can only be called by instances of the class and its subclasses
    * private: can only be called by instances of a class

    private # --> All the methods below will be private
        ...
    
    protected # --> All the methods below will be protected
        ...
    
=end